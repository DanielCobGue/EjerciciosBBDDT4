CREATE TABLE DEPARTAMENTO(
	CLAVE_DEPTO NUMBER(1),
	NOMBRE VARCHAR(30) NOT NULL,
	PRESUPUESTO NUMBER(5) NOT NULL,
	CONSTRAINT PK_DEPARTAMENTO PRIMARY KEY (CLAVE_DEPTO)
);
CREATE TABLE EMPLEADOS(
	CLAVEEMPLEADO VARCHAR(5),
	NOMBRE VARCHAR(30) NOT NULL,
	AP1 VARCHAR(35) NOT NULL,
    AP2 VARCHAR(35) NULL,
    CLAVEDEPTO NUMBER(1)NOT NULL,
    CONSTRAINT PK_EMPLEADOS PRIMARY KEY (CLAVEEMPLEADO),
    CONSTRAINT FK_EMPLEADOS FOREIGN KEY CLAVEDEPTO REFERENCES DEPARTAMENTO(CLAVE_DEPTO)
);

